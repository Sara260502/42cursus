#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		count;
	t_list	*current;

	count = 0;
	current = lst;
	while (current->next != NULL)
	{
		current = current->next;
		count++;
	}
	if (current->size == NULL)
		count++;
	return (count);
}

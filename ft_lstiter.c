#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst == NULL || f == NULL)
		return (NULL);
	while (lst && f)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
